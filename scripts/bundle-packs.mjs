/**
 * Reads the 3 built-in writing packs from the plan folder and generates
 * src/lib/builtinPacks.ts with all content embedded as a TypeScript constant.
 *
 * Run: node scripts/bundle-packs.mjs
 */

import { readFileSync, readdirSync, statSync, writeFileSync } from "fs";
import { join, extname, basename } from "path";

const PACKS_DIR =
  "/Users/shrimp/Library/CloudStorage/GoogleDrive-yuchenwang1990@gmail.com/My Drive/Claude Projects/projects/09_biling/packs";

const DIR_TO_CATEGORY = {
  templates:     "template",
  materials:     "material",
  inspirations:  "inspiration",
  writing_rules: "writing_rule",
  references:    "reference",
};

function readPack(packDir) {
  const manifestPath = join(packDir, "manifest.json");
  const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));

  const items = [];

  for (const [dirName, category] of Object.entries(DIR_TO_CATEGORY)) {
    const dir = join(packDir, dirName);
    let files;
    try {
      files = readdirSync(dir);
    } catch {
      continue;
    }
    for (const file of files.sort()) {
      const ext = extname(file);
      if (![".md", ".txt", ".json"].includes(ext)) continue;
      const filePath = join(dir, file);
      if (!statSync(filePath).isFile()) continue;
      const title = basename(file, ext).replace(/_/g, " ");
      const content = readFileSync(filePath, "utf8");
      items.push({ category, title, content });
    }
  }

  return { manifest, items };
}

const packNames = readdirSync(PACKS_DIR).filter(
  (n) => statSync(join(PACKS_DIR, n)).isDirectory()
);

const packs = packNames.map((name) => readPack(join(PACKS_DIR, name)));

const tsContent = `// AUTO-GENERATED by scripts/bundle-packs.mjs — do not edit manually
import type { PackManifest, PackCategory } from "../store/writingPacksStore";

export interface BuiltinPackItem {
  category: PackCategory;
  title: string;
  content: string;
}

export interface BuiltinPack {
  manifest: PackManifest;
  items: BuiltinPackItem[];
}

export const BUILTIN_PACKS: BuiltinPack[] = ${JSON.stringify(packs, null, 2)};
`;

const outPath = new URL("../src/lib/builtinPacks.ts", import.meta.url).pathname;
writeFileSync(outPath, tsContent, "utf8");
console.log(`Written ${outPath}`);
console.log(`Packs: ${packs.map((p) => p.manifest.name).join(", ")}`);
console.log(`Total items: ${packs.reduce((s, p) => s + p.items.length, 0)}`);
